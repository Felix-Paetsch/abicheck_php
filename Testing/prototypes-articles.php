<p>Die Addition ist eine der vier Grundrechenarten. Sie wird umgangssprachlich auch als „Plus-Rechnen“ bezeichnet. Beim Addieren werden zwei (oder mehr) Zahlen, auch Summanden gennant, durch ein „\(+\)“ verbunden. Das Ergebnis wird als Summe bezeichnet.</p>
<div class="important">
<li>$$\begin{eqnarray}&3+5&=&8\\[5pt]&Summand + Summand &=& Summe\end{eqnarray}$$</li>
</div>
<p>Man sagt: „Erster Summand + zweiter Summand (ist) gleich Summe.“ <br>Addition basiert auf dem Vorgang des Zählens. In den natürlichen Zahlen kann man Addition durchführen, indem man zuerst den einen Summanden zählt und dann den zweiten Summanden dazu.</p>
<div class="example">
<li>Rechnet man \(2+4\) kann man bei \(2\) starten und \(4\) mal eins dazu zählen:</li>
<li>\(2 \rightarrow 3 \rightarrow 4 \rightarrow 5 \rightarrow 6\)</li>
<li>Das heißt: \(2+4=6\).</li>
